def _request_id(thread_ts: str) -> str:
    """Derive a short, human-readable request ID from a Slack thread_ts.
    e.g. '1741234567.123456' → '20260311-4567'
    """
    try:
        ts_int = int(float(thread_ts))
        date_str = datetime.utcfromtimestamp(ts_int).strftime("%Y%m%d")
        short = str(ts_int)[-4:]
        return f"{date_str}-{short}"
    except Exception:
        return ""

