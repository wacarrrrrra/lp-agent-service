@app.post("/slack/events")
async def slack_events(request: Request):
    raw_body = await request.body()
    verify_slack_signature(
        raw_body,
        request.headers.get("X-Slack-Request-Timestamp", ""),
        request.headers.get("X-Slack-Signature", ""),
    )
    body = json.loads(raw_body.decode("utf-8") or "{}")

    if body.get("type") == "url_verification":
        return JSONResponse({"challenge": body.get("challenge")})

    event = body.get("event", {}) or {}
    if event.get("type") != "message":
        return JSONResponse({"ok": True})
    if event.get("subtype") and "[LP-REQUEST]" not in (event.get("text") or ""):
        return JSONResponse({"ok": True})

    thread_ts = event.get("thread_ts") or event.get("ts")
    user_id = event.get("user") or ""
    text = (event.get("text") or "").strip()

    logger.info("EVENT user=%s thread_ts=%s jobs_loaded=%s bart_done=%s text_preview=%s",
                user_id, thread_ts, list(JOBS.keys()), "BART_DONE" in text, text[:80])

    job = JOBS.get(thread_ts)

    # If job not in memory (e.g. Render restarted) but Bart posted BART_DONE,
    # reconstruct the job from the thread's starter message
    if not job and user_id == BART_USER_ID and "BART_DONE" in text and thread_ts:
        logger.info("Job not in memory — attempting reconstruction from thread %s", thread_ts)
        try:
            bart_channel = SEM_LP_REQUESTS_CHANNEL or SLACK_DEFAULT_CHANNEL
            thread_messages = await fetch_thread_messages(bart_channel, thread_ts)
            starter = next(
                (m for m in thread_messages if "Search term:" in (m.get("text") or "")),
                None
            )
            if starter:
                starter_text = starter.get("text", "")
                def _parse_field(label: str) -> str:
                    m = re.search(rf'\*{label}:\*\s*(.+)', starter_text)
                    return m.group(1).strip() if m else ""
                search_term = _parse_field("Search term")
                job = {
                    "request_id": _parse_field("Request ID") or generate_request_id(),
                    "slug": slugify(search_term),
                    "bart_channel": bart_channel,
                    "requester_channel": SEM_LP_BUILD_KITS_CHANNEL or SLACK_DEFAULT_CHANNEL,
                    "fields": {
                        "search_term": search_term,
                        "primary_cta": _parse_field("Primary CTA") or "Demo",
                        "intent": _parse_field("Intent") or "Commercial",
                        "primary_audience": _parse_field("Primary Audience") or "",
                        "secondary_keywords": None,
                        "offer": "", "must_include": "", "must_not_say": "",
                    },
                    "secondary_keywords": [],
                    "awaiting": "bart",
                }
                JOBS[thread_ts] = job
                _save_jobs()
                logger.info("Job reconstructed for thread %s search_term=%s", thread_ts, search_term)
        except Exception as e:
            logger.warning("Job reconstruction failed: %s", e)

    # ── Slack Workflow trigger ──────────────────────────────────────────────
    # Workflows (used for Slack Connect / external guests) post a structured
    # [LP-REQUEST] message to #sem-lp-requests.  We detect it here so the
    # workflow can be set up entirely in the Slack UI — no slash command needed.
    if "[LP-REQUEST]" in text and not job:
        wf_fields = parse_workflow_request(text)
        if wf_fields:
            wf_search_term = wf_fields["search_term"]
            wf_request_id = generate_request_id()
            wf_slug = slugify(wf_search_term)
            wf_secondary_keywords = parse_secondary_keywords(wf_fields.get("secondary_keywords"))
            bart_channel = SEM_LP_REQUESTS_CHANNEL or SLACK_DEFAULT_CHANNEL
            requester_channel = SEM_LP_BUILD_KITS_CHANNEL or SLACK_DEFAULT_CHANNEL

            async def run_workflow_pipeline(
                _fields=wf_fields,
                _request_id=wf_request_id,
                _slug=wf_slug,
                _secondary_keywords=wf_secondary_keywords,
                _bart_channel=bart_channel,
                _requester_channel=requester_channel,
            ):
                try:
                    await post_message(
                        _requester_channel,
                        f"Got it — drafting `{_fields['search_term']}` now. "
                        f"I'll post the package here when it's ready. _(Request ID: {_request_id})_",
                    )

                    starter = (
                        f"🚀 *LP request started (via Workflow)*\n"
                        f"*Request ID:* {_request_id}\n"
                        f"*Search term:* {_fields['search_term']}\n"
                        f"*Secondary keywords:* {', '.join(_secondary_keywords) if _secondary_keywords else '—'}\n"
                        f"*Primary CTA:* {_fields['primary_cta']}\n"
                        f"*Intent:* {_fields['intent']}\n"
                        f"*Primary Audience:* {_fields.get('primary_audience') or '—'}\n"
                    )
                    bart_thread_ts = await post_message(_bart_channel, starter)

                    JOBS[bart_thread_ts] = {
                        "request_id": _request_id,
                        "slug": _slug,
                        "bart_channel": _bart_channel,
                        "requester_channel": _requester_channel,
                        "fields": _fields,
                        "secondary_keywords": _secondary_keywords,
                        "awaiting": "bart",
                        "bart_output": None,
                    }
                    _save_jobs()

                    await post_message(
                        _bart_channel,
                        bart_prompt(_fields["search_term"], _fields["primary_cta"], _fields["intent"], _secondary_keywords),
                        thread_ts=bart_thread_ts,
                    )
                    await post_message(
                        _bart_channel,
                        "⏳ Waiting for Bart… pipeline continues automatically after `BART_DONE`.",
                        thread_ts=bart_thread_ts,
                    )

                except Exception as e:
                    logger.exception("Workflow pipeline failed to start: %s", e)
                    try:
                        await post_message(_requester_channel, f"❌ Pipeline failed to start: `{e}`")
                    except Exception:
                        pass

            asyncio.create_task(run_workflow_pipeline())
            return JSONResponse({"ok": True})
    # ───────────────────────────────────────────────────────────────────────

    if not job:
        return JSONResponse({"ok": True})

    if job.get("awaiting") == "bart" and user_id == BART_USER_ID and "BART_DONE" in text:
        job["bart_output"] = text
        job["awaiting"] = "generating"
        JOBS[thread_ts] = job
        _save_jobs()

        bart_channel = job["bart_channel"]
        requester_channel = job["requester_channel"]
        request_id = job["request_id"]
        fields = job["fields"]
        slug = job["slug"]
        secondary_keywords = job.get("secondary_keywords", [])

        async def continue_pipeline():
            try:
                # Step 1 — Accumulate full multi-part brief from the Bart thread
                await post_message(
                    bart_channel, "📖 Collecting full brief from thread…", thread_ts=thread_ts
                )
                messages = await fetch_thread_messages(bart_channel, thread_ts)
                bart_brief = accumulate_bart_brief(messages, BART_USER_ID) or text

                # Step 2 — Generate brand-compliant SVGs
                image_refs = parse_image_refs(bart_brief)
                svgs: Dict[str, str] = {}
                if image_refs:
                    await post_message(
                        bart_channel,
                        f"🎨 Generating {len(image_refs)} brand-compliant SVG(s)…",
                        thread_ts=thread_ts,
                    )
                    svgs = await generate_svgs(bart_brief, slug)

                # Step 3 — Copywrite + build LP package
                await post_message(
                    bart_channel, "✍️ Step 3/4: Conversion copywriting…", thread_ts=thread_ts
                )
                lp_package = await generate_full_lp(fields, bart_brief, svgs, secondary_keywords, request_id=_request_id(thread_ts))
                final_slug = lp_package["slug"]

                # Step 4 — Commit to GitHub
                files_to_commit: Dict[str, str] = {
                    f"generated-pages/{final_slug}/index.html": lp_package["html"],
                    f"generated-pages/{final_slug}/metadata.json": lp_package["metadata_json"],
                    f"generated-pages/{final_slug}/brief.md": bart_brief,
                }
                for svg_name, svg_content in svgs.items():
                    files_to_commit[f"generated-pages/{final_slug}/images/{svg_name}"] = svg_content
                if LOGO_SVG_BLACK and not LOGO_SVG_BLACK.startswith("[Missing"):
                    files_to_commit[f"generated-pages/{final_slug}/images/dataHub_logo_color_black.svg"] = LOGO_SVG_BLACK

                kit_url = (
                    f"https://github.com/{GITHUB_REPO}/tree/{GITHUB_BRANCH}"
                    f"/generated-pages/{final_slug}"
                )

                if GITHUB_TOKEN and GITHUB_REPO:
                    await post_message(
                        bart_channel, "📤 Committing files to GitHub…", thread_ts=thread_ts
                    )
                    all_ok, failed_paths = await github_commit_files(
                        GITHUB_REPO, files_to_commit,
                        f"Add LP package: {final_slug}", GITHUB_BRANCH,
                    )
                    if all_ok:
                        internal_status = f"✅ Committed to GitHub: {kit_url}"
                    else:
                        internal_status = (
                            f"⚠️ Some files failed to commit:\n"
                            + "\n".join(f"• `{p}`" for p in failed_paths)
                        )
                else:
                    all_ok = False
                    internal_status = "⚠️ GitHub commit skipped — GITHUB_TOKEN or GITHUB_REPO not set"

                # Internal confirmation in the private Bart thread
                await post_message(bart_channel, internal_status, thread_ts=thread_ts)

                # Agency-facing confirmation in #sem-lp-build-kits
                _preview = lp_package.get("pages_url", "")
                agency_msg = (
                    f"✅ *LP package ready*\n"
                    f"*Search term:* {fields['search_term']}\n"
                    f"*Slug:* `{final_slug}`\n"
                    f"*CTA:* {fields.get('primary_cta','Demo')} | "
                    f"*Intent:* {fields.get('intent','Commercial')}\n"
                    f"*Files:* {kit_url}"
                    + (f"\n*Preview:* {_preview}" if _preview else "")
                )
                await post_message(requester_channel, agency_msg)

                job["awaiting"] = "done"
                JOBS[thread_ts] = job
                _save_jobs()

            except Exception as e:
                logger.exception("Pipeline continuation failed: %s", e)
                try:
                    await post_message(bart_channel, f"❌ Pipeline failed: `{e}`", thread_ts=thread_ts)
                    await post_message(requester_channel, f"❌ LP generation failed for `{slug}`: `{e}`")
                except Exception:
                    pass
                job["awaiting"] = "error"
                JOBS[thread_ts] = job
                _save_jobs()

        asyncio.create_task(continue_pipeline())

    return JSONResponse({"ok": True})
