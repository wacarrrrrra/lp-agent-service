    try:
        writer_json = json.loads(writer_raw)
    except Exception:
        # Try extracting just the outermost JSON object
        match = re.search(r'\{[\s\S]*\}', writer_raw)
        if match:
            try:
                writer_json = json.loads(match.group(0))
            except Exception:
                # Last resort: ask Claude to fix it
                fix_prompt = (
                    "The following text is meant to be valid JSON but has a syntax error. "
                    "Fix it and return ONLY the corrected JSON with no explanation:\n\n"
                    + match.group(0)[:6000]
                )
                fixed = await claude_text(fix_prompt, max_tokens=8000)
                fixed = re.sub(r'^```(?:json)?\s*', '', fixed.strip(), flags=re.IGNORECASE)
                fixed = re.sub(r'\s*```$', '', fixed.strip())
                writer_json = json.loads(fixed)
        else:
            raise ValueError(f"Writer did not return valid JSON: {writer_raw[:500]}")

    html_prompt = build_lp_html_prompt(fields, writer_json, svgs, secondary_keywords)
    page_html = await claude_text(html_prompt, max_tokens=8000)

    # Claude returns body content only — strip any accidental wrapper tags
    page_html = re.sub(r'(?i)<!doctype[^>]*>', '', page_html).strip()
    page_html = re.sub(r'(?i)</?html[^>]*>', '', page_html).strip()
    # Strip <head>...</head> if Claude included one despite instructions
    page_html = re.sub(r'(?i)<head[\s\S]*?</head>', '', page_html).strip()
    # Strip accidental <body> / </body> wrapper tags
    page_html = re.sub(r'(?i)<body[^>]*>', '', page_html).strip()
    page_html = re.sub(r'(?i)</body>', '', page_html).strip()

    # Inject hardcoded trust strip — replace placeholder if present, otherwise insert after first </section>
    if '<!-- TRUST_STRIP_PLACEHOLDER -->' in page_html:
        page_html = page_html.replace('<!-- TRUST_STRIP_PLACEHOLDER -->', TRUST_STRIP_HTML)
    else:
        # Fallback: insert after the first closing </section> (end of hero)
        page_html = page_html.replace('</section>', f'</section>\n{TRUST_STRIP_HTML}', 1)

    # Guarantee footer — if Claude hit the token limit, append the template footer verbatim
    if '<footer' not in page_html.lower() and TEMPLATE_FOOTER_HTML:
        page_html = page_html + '\n' + TEMPLATE_FOOTER_HTML

    # Assemble metadata and full page
    seo = writer_json.get("seo_json", {})
    raw_slug = (seo.get("slug") or slugify(fields["search_term"])).strip("/")
    if request_id:
        raw_slug = f"{raw_slug}-{request_id}"
    metadata = {
        "slug": raw_slug or slugify(fields["search_term"]),
        "primary_search_term": fields["search_term"],
        "intent_type": seo.get("intent_type") or fields.get("intent", ""),
        "cta_type": seo.get("cta_type") or fields.get("primary_cta", ""),
        "hubspot_form_id": seo.get("hubspot_form_id") or _hubspot_form_id(fields.get("primary_cta", "Demo")),
        "title_tag": seo.get("title_tag") or "",
        "meta_description": seo.get("meta_description") or "",
        "h1": seo.get("h1") or "",
        "generated_date": datetime.now().strftime("%Y-%m-%d"),
        "bart_done": True,
    }

    # Wrap body content with the template head (CSS included, no token waste)
    head = build_page_head(metadata["title_tag"], metadata["meta_description"])
    full_html = f"<!doctype html>\n<html lang=\"en\">\n{head}\n<body>\n{page_html}\n</body>\n</html>"

    pages_url = (
        f"{GITHUB_PAGES_BASE}/generated-pages/{metadata['slug']}/"
        if GITHUB_PAGES_BASE else ""
    )
    return {
        "html": full_html,
        "metadata_json": json.dumps(metadata, indent=2),
        "writer_json": writer_json,
        "slug": metadata["slug"],
        "pages_url": pages_url,
    }

