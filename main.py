async def _handle_build_modal(payload: dict, view: dict) -> JSONResponse:
    """Handle /sem-lp-build modal submission — manual pipeline trigger from a Bart thread."""
    values = view.get("state", {}).get("values", {})

    def gv(block_id: str, action_id: str) -> Optional[str]:
        block = values.get(block_id, {})
        action = block.get(action_id, {})
        if "value" in action:
            return action.get("value")
        sel = action.get("selected_option")
        return sel.get("value") if sel else None

    thread_link = (gv("thread_link_block", "thread_link") or "").strip()
    search_term = (gv("search_term_block", "search_term") or "").strip()
    primary_cta = gv("primary_cta_block", "primary_cta") or "Demo"
    intent = gv("intent_block", "intent") or "Commercial"
    primary_audience = gv("primary_audience_block", "primary_audience") or ""
    secondary_keywords = parse_secondary_keywords(gv("secondary_keywords_block", "secondary_keywords"))
    must_include = gv("must_include_block", "must_include") or ""

    errors: Dict[str, str] = {}
    if not thread_link:
        errors["thread_link_block"] = "Paste the Slack link to the Bart thread."
    if not search_term:
        errors["search_term_block"] = "Search term is required."

    _parsed_channel, thread_ts = parse_slack_thread_link(thread_link)
    if thread_link and not thread_ts:
        errors["thread_link_block"] = "Couldn't parse that link — right-click the thread message and choose Copy link."
    # Always fetch from the private Bart channel regardless of what's in the link
    bart_channel = SEM_LP_REQUESTS_CHANNEL or _parsed_channel

    if errors:
        return JSONResponse({"response_action": "errors", "errors": errors})

    user_id = (payload.get("user") or {}).get("id") or "unknown"
    requester_channel = (view.get("private_metadata") or "").strip() or SEM_LP_BUILD_KITS_CHANNEL or SLACK_DEFAULT_CHANNEL
    request_id = generate_request_id()
    slug = slugify(search_term)

    fields = {
        "search_term": search_term,
        "primary_cta": primary_cta,
        "intent": intent,
        "primary_audience": primary_audience,
        "secondary_keywords": None,
        "must_include": must_include,
        "offer": "",
        "must_not_say": "",
    }

    async def run_manual_build():
        try:
            await post_message(
                requester_channel,
                f"🔧 Manual build started — `{search_term}` _(Request ID: {request_id})_\n"
                f"Fetching Bart's thread and building now…",
            )

            # Fetch and accumulate full Bart brief from the thread
            messages = await fetch_thread_messages(bart_channel, thread_ts)
            logger.info("MANUAL BUILD thread_ts=%s channel=%s messages_found=%s user_ids=%s",
                        thread_ts, bart_channel, len(messages),
                        [m.get("user") for m in messages])

            bart_brief = accumulate_bart_brief(messages, BART_USER_ID)

            # Fallback: if BART_USER_ID doesn't match, use all non-bot messages in the thread
            if not bart_brief:
                logger.warning("No Bart messages found by user ID — falling back to all thread messages")
                parts = [
                    (m.get("text") or "").strip()
                    for m in messages
                    if (m.get("text") or "").strip() and not m.get("bot_id")
                ]
                bart_brief = "\n\n---\n\n".join(parts)

            if not bart_brief:
                await post_message(
                    requester_channel,
                    f"⚠️ No messages found in that thread. Double-check the link and try again."
                )
                return

            # Generate SVGs
            image_refs = parse_image_refs(bart_brief)
            svgs: Dict[str, str] = {}
            if image_refs:
                await post_message(
                    requester_channel,
                    f"🎨 Generating {len(image_refs)} SVG(s)…",
                )
                svgs = await generate_svgs(bart_brief, slug)

            # Generate LP package
            await post_message(requester_channel, "✍️ Conversion copywriting + HTML build…")
            lp_package = await generate_full_lp(fields, bart_brief, svgs, secondary_keywords, request_id=_request_id(thread_ts))
            final_slug = lp_package["slug"]

            # Commit to GitHub
            files_to_commit: Dict[str, str] = {
                f"generated-pages/{final_slug}/index.html": lp_package["html"],
                f"generated-pages/{final_slug}/metadata.json": lp_package["metadata_json"],
                f"generated-pages/{final_slug}/brief.md": bart_brief,
            }
            for svg_name, svg_content in svgs.items():
                files_to_commit[f"generated-pages/{final_slug}/images/{svg_name}"] = svg_content
            if LOGO_SVG_BLACK and not LOGO_SVG_BLACK.startswith("[Missing"):
                files_to_commit[f"generated-pages/{final_slug}/images/dataHub_logo_color_black.svg"] = LOGO_SVG_BLACK

            kit_url = (
                f"https://github.com/{GITHUB_REPO}/tree/{GITHUB_BRANCH}"
                f"/generated-pages/{final_slug}"
            )

            if GITHUB_TOKEN and GITHUB_REPO:
                await post_message(requester_channel, "📤 Committing to GitHub…")
                all_ok, failed_paths = await github_commit_files(
                    GITHUB_REPO, files_to_commit,
                    f"Add LP package: {final_slug}", GITHUB_BRANCH,
                )
                if all_ok:
                    preview = lp_package.get("pages_url", "")
                    result_msg = (
                        f"✅ *LP package ready*\n"
                        f"*Search term:* {search_term}\n"
                        f"*Slug:* `{final_slug}`\n"
                        f"*Files:* {kit_url}"
                        + (f"\n*Preview:* {preview}" if preview else "")
                    )
                else:
                    result_msg = (
                        f"⚠️ *LP generated* — `{final_slug}` — some files failed:\n"
                        + "\n".join(f"• `{p}`" for p in failed_paths)
                    )
            else:
                result_msg = f"✅ *LP package generated* — `{final_slug}` (GitHub not configured)"

            await post_message(requester_channel, result_msg)

        except Exception as e:
            logger.exception("Manual build failed: %s", e)
            try:
                await post_message(requester_channel, f"❌ Manual build failed: `{e}`")
            except Exception:
                pass

    asyncio.create_task(run_manual_build())
    return JSONResponse({"response_action": "clear"})


async def _handle_interactivity(request: Request):
    raw_body = await request.body()
    logger.info("INTERACTIVITY hit bytes=%s path=%s", len(raw_body), request.url.path)

    try:
        verify_slack_signature(
            raw_body,
            request.headers.get("X-Slack-Request-Timestamp", ""),
            request.headers.get("X-Slack-Signature", ""),
        )

        form = await request.form()
        payload_raw = form.get("payload")
        if not payload_raw:
            return JSONResponse({"ok": True})

        payload = json.loads(payload_raw)
        if payload.get("type") != "view_submission":
            return JSONResponse({"ok": True})

        view = payload.get("view", {})
        callback_id = view.get("callback_id")

        if callback_id == "lp_build_modal":
            return await _handle_build_modal(payload, view)

        if callback_id != "lp_request_modal":
            return JSONResponse({"response_action": "clear"})

        fields = extract_modal_values(view.get("state", {}))

        errors = {}
        if not fields.get("search_term"):
            errors["search_term_block"] = "Search term is required."
        if not fields.get("primary_cta"):
            errors["primary_cta_block"] = "Primary CTA is required."
        if not fields.get("intent"):
            errors["intent_block"] = "Intent is required."
        if not fields.get("primary_audience"):
            errors["primary_audience_block"] = "Primary Audience is required."
        if errors:
            return JSONResponse({"response_action": "errors", "errors": errors})

        request_id = generate_request_id()
        slug = slugify(fields["search_term"])
        user_id = (payload.get("user") or {}).get("id") or "unknown"

        # Channel where the slash command was run (agency-facing, e.g. #sem-lp-build-kits)
        requester_channel = (view.get("private_metadata") or "").strip() or SLACK_DEFAULT_CHANNEL or ""
        if not requester_channel:
            return JSONResponse({"response_action": "clear"})

        # Private Bart channel — fall back to requester_channel if not configured
        bart_channel = SEM_LP_REQUESTS_CHANNEL or requester_channel

        secondary_keywords = parse_secondary_keywords(fields.get("secondary_keywords"))

        async def run_pipeline():
            try:
                # Acknowledge in the agency channel immediately
                await post_message(
                    requester_channel,
                    f"Got it — drafting `{fields['search_term']}` now. "
                    f"I'll post the package here when it's ready. _(Request ID: {request_id})_",
                )

                # Start the Bart thread in the private channel
                starter = (
                    f"🚀 *LP request started*\n"
                    f"*Request ID:* {request_id}\n"
                    f"*Search term:* {fields['search_term']}\n"
                    f"*Secondary keywords:* {', '.join(secondary_keywords) if secondary_keywords else '—'}\n"
                    f"*Primary CTA:* {fields['primary_cta']}\n"
                    f"*Intent:* {fields['intent']}\n"
                    f"*Primary Audience:* {fields['primary_audience']}\n"
                    f"*Requester:* <@{user_id}>"
                )
                bart_thread_ts = await post_message(bart_channel, starter)

                JOBS[bart_thread_ts] = {
                    "request_id": request_id,
                    "slug": slug,
                    "bart_channel": bart_channel,
                    "requester_channel": requester_channel,
                    "user_id": user_id,
                    "fields": fields,
                    "secondary_keywords": secondary_keywords,
                    "awaiting": "bart",
                    "bart_output": None,
                }
                _save_jobs()

                await post_message(
                    bart_channel,
                    bart_prompt(fields["search_term"], fields["primary_cta"], fields["intent"], secondary_keywords),
                    thread_ts=bart_thread_ts,
                )
                await post_message(
                    bart_channel,
                    "⏳ Waiting for Bart… pipeline continues automatically after `BART_DONE`.",
                    thread_ts=bart_thread_ts,
                )

            except Exception as e:
                logger.exception("run_pipeline failed: %s", e)
                try:
                    await post_message(requester_channel, f"❌ Pipeline failed to start: `{e}`")
                except Exception:
                    pass

        asyncio.create_task(run_pipeline())
        return JSONResponse({"response_action": "clear"})

    except Exception as e:
        logger.exception("Interactivity handler error: %s", e)
        return JSONResponse({"response_action": "clear"})

